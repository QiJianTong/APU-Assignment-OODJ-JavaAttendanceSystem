    private void jbtnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnsaveActionPerformed
       
        
       
        Admin admin = new Admin();//switch
       
        switch((String)jcbregister.getSelectedItem()){
            case "Student":
                admin.registerstu(jtxtname.getText(), jtxtpassport.getText(), null,(String)jcbcourse.getSelectedItem(),jtxtphone.getText());
                jtxtname.setText("");
                jtxtpassport.setText("");
                break;
               
            case "Lecturer":
                admin.registerlect(jtxtname.getText(), jtxtpassport.getText(),(String)jcbcourse.getSelectedItem(),jtxtphone.getText());
                jtxtname.setText("");
                jtxtpassport.setText("");
                break;
       
       }
            // dispose in front window
            setDefaultCloseOperation(javax.swing.WindowConstants.DISPOSE_ON_CLOSE);
    
      
      
        
    }//GEN-LAST:event_jbtnsaveActionPerformed

